// This requires a function that is capable of transforming a number into a string i.e changing the data type from number to a string.
// This can be dont by invoking a toString method on the string.

// Some examples that show what should be acheived are:
// 123 --> "123"
// 999 --> "999"

function numberToString(num) {
    // Return a string of the number here!
    return num.toString();
}

